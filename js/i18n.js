// 获取当前语言（始终从localStorage读取最新值）
function getCurrentLang() {
    return localStorage.getItem('language') || 'en';
}

// 获取翻译文本
function t(key) {
    const currentLang = getCurrentLang();
    const keys = key.split('.');
    let value = translations[currentLang];

    for (const k of keys) {
        value = value[k];
        if (!value) return key;
    }

    return value;
}

// 切换语言
function switchLanguage(lang) {
    if (lang && (lang === 'zh' || lang === 'en')) {
        localStorage.setItem('language', lang);
        updatePageLanguage();
    }
}

// 更新页面语言
function updatePageLanguage() {
    const currentLang = getCurrentLang();

    // 更新所有带有 data-i18n 属性的元素
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        element.textContent = t(key);
    });

    // 更新HTML内容（支持<br>等标签）
    document.querySelectorAll('[data-i18n-html]').forEach(element => {
        const key = element.getAttribute('data-i18n-html');
        element.innerHTML = t(key);
    });

    // 更新placeholder
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        element.placeholder = t(key);
    });

    // 更新语言选择器状态
    const langZh = document.getElementById('lang-zh');
    const langEn = document.getElementById('lang-en');
    if (langZh && langEn) {
        if (currentLang === 'zh') {
            langZh.classList.add('active');
            langEn.classList.remove('active');
        } else {
            langZh.classList.remove('active');
            langEn.classList.add('active');
        }
    }

    // 触发自定义事件，让其他模块知道语言已切换
    document.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang: currentLang } }));
}

// 页面加载前先隐藏内容，防止语言切换闪烁
document.documentElement.style.opacity = '0';

// 页面加载时初始化语言
document.addEventListener('DOMContentLoaded', function() {
    updatePageLanguage();

    // 语言更新完成后显示页面
    setTimeout(function() {
        document.documentElement.style.transition = 'opacity 0.15s ease-in';
        document.documentElement.style.opacity = '1';
    }, 50);

    // 监听DOM变化，当新内容加载时自动应用语言
    let updateTimeout;
    const observer = new MutationObserver(function(mutations) {
        // 使用防抖避免频繁更新
        clearTimeout(updateTimeout);
        updateTimeout = setTimeout(function() {
            const hasNewContent = mutations.some(mutation =>
                mutation.addedNodes.length > 0 &&
                Array.from(mutation.addedNodes).some(node =>
                    node.nodeType === 1 && // 只处理元素节点
                    (node.querySelector('[data-i18n]') || node.hasAttribute('data-i18n'))
                )
            );
            if (hasNewContent) {
                updatePageLanguage();
            }
        }, 100);
    });

    // 监听整个body的变化
    observer.observe(document.body, {
        childList: true,
        subtree: true
    });
});

// 导出到全局
window.t = t;
window.switchLanguage = switchLanguage;
window.getCurrentLang = getCurrentLang;
